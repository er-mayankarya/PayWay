import mongoose from "mongoose";
import 'dotenv/config';

mongoose.connect(process.env.DATABASE_URL);

const userSchema = new mongoose.Schema({  
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true,
        minLength: 3,
        maxLength: 30
    },
    password: {
        type: String,
        required: true,
        minLength: 6
    },
    firstName: {
        type: String,
        required: true,
        trim: true,
        maxLength: 50
    },
    lastName: {
        type: String,
        required: true,
        trim: true,
        maxLength: 50
    }
});

// Create a model from the schema
export const User = mongoose.model('User', userSchema);